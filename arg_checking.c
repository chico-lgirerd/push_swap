int	ft_duplicates(int *tab)
{
	int	i;
	int	j;

	i = 0;
	while (tab[i] != '\0')
	{
		j = 0;
		while (tab[j] != '\0')
		{
			if (tab[i] == tab[j] && (i != j))
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
